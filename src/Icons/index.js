/* eslint-enable */

export function asIcon(Icon) {
  return class extends Component {
    render() {
      const toTheme = theme => ({ dark: '#5C8BCC', light: '#EEE' }[theme]);
      const defaultProps = { width: 16, height: 16, fill: toTheme('dark') };
      const props = { ...defaultProps, ...this.props };
      return <Icon {...props} />;
    }
  };
}

const gridStyles = {
  display: 'flex',
  flexWrap: 'wrap',
  justifyContent: 'space-between',
  margin: '0 auto',
  maxWidth: '500px'
};

export const IconGrid = ({ children }) => {
  const childrenWithProps = React.Children.map(children, (child, i) =>
    React.cloneElement(child, { key: i, style: { padding: '20px' } })
  );
  return <div style={gridStyles}>{childrenWithProps}</div>;
};
